package eu.ydp.empiria.player.client.controller.variables.processor.results.model;

public class ConstantVariables {

    private int todo = 0;

    public ConstantVariables() {
    }

    public ConstantVariables(int todo) {
        this.todo = todo;
    }

    public int getTodo() {
        return todo;
    }

    public void setTodo(int todo) {
        this.todo = todo;
    }
}
